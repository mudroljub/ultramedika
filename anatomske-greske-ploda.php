<h1>Zašto je važno otkrivanje anatomskih grešaka u razvoju ploda</h1>

<p><b>Trudnoća je povremeno praćena neadekvatnim razvojem ploda.</b></p>

<p>Razvoj ploda može biti poremećen zbog greške  na hromozomima ili zbog neadekvatnog razvoja organa ploda tj. anatomskih grešaka.</p>

<p>Hromozomske greške se otkrivaju iz krvi trudnice a anatomske ne. </p>

<p>Za razliku od toga ultrazvukom se otkrivaju i hromozomske i anatomske greske.</p>

<p>Anatomske greške su znatno češće. Tako su urođene srčane mane 6 puta češće  od najčešće hromozomske greške tj. trizomije 21. para hromozoma.</p>

<p>Bez ultrazvučnog pregleda nema otkrivanja anatomskih grešaka  ploda.</p>

<p>Otkrivanje grešaka je uslovljeno razvojem ploda koje traje 9 meseci.</p>

<p>Kalendar ultrazvučnog pregleda uslovljen je razvojem tj embriogenezom ploda.</p>

<p>1. U prvih 3 meseca trudnoće otkrivamo najveći broj plodova sa hromozomskim greškama i najčešćom anatomskom greškom tj. urođenom srčanom manom. Pored toga tu se mogu otkriti greške u prvoj fazi razvoja mozga, koštanih struktura, rascepa nepca itd.</p>

<p>2. Po završetku 4. meseca trudnoće može se otkriti najveći broj anatomskih grešaka organa s obzirom da je 80 procenata razvoja ploda tek tada završeno.
Takođe prisutni ultrazvučni  markeri u 16. nedelji trudnoće mogu dovesti u sumnju postojanje oko 90 posto plodova sa hromozomskim greškama.</p>

<p>I 3. važna stvar je  proverava ultrazvukom cirkulacija posteljice . Ako ona nije odgovarajuća postoji mogućnost zastoja rasta ploda. U tom slučaju se mora ordinirati terapija da bi se sprečio zastoj rasta ploda.</p>

<p>U 6. i 8. mesecu trudnoće proveravamo ispravnost još ostalih struktura koje tek tada završavaju razvoj. Tu se podrazumeva provera funkcije bubrega, provere krvnih sudova mozga i njegove treće faze razvoja kao i spoljnih genitalija i cevastih organa.</p>

<p>Zadnjih mesec dana je rezervisana ultrazvučna procena završetka razvoja pluća.
Postepen razvoj  ploda zahteva ultrazvučne preglede u određenoj starosti trudnoće.</p>

<p>Svaki pregled mora biti ekspertni ako želimo da otkrijemo blagovremeno neku grešku ploda. Nakon toga donesimo odluku da li trudnoću treba prekinuti ili nastavati. Tačna procena greške u razvoju ploda je ključna u odluci da li treba prekinuti ili nastaviti trudnoću.
Tačna dijagnoza zdravog ili ploda sa nekom greškom uslovljena je iskustvom i znanjem  doktora u otkrivanju urođenih grešaka ploda.</p>

<p>Drugi važan faktor je da ultrazvučni aparat poseduje odgovarajuće sonde i softvere prema starosti ploda. Aparat mora posedovati kvalitetnu 2D, 3D i 4D sliku. Takođe mora aparat posedovati kompjuterske alate kao xPlane, STIC, CD, CPA, elastografiju itd.</p>

<p>Ultrazvuk mora posedovati stalnu kontrolu termalnog i mehaničkog indeksa ultrazvučnih talasa. Svaka starost ploda zahteva drugačije upotrebljene ultrazvučne energije da bi pregled bio bezbedan za plod. Dokumentacija ultrazvučnog pregled mora sadržavati tabelarni prikaz  merenja anatomskih struktura ploda i izdavanje odgovarajućih ultrazvučnih slika koje potvrđuju valjanost ultrazvučnog pregleda trudnoće. </p>

